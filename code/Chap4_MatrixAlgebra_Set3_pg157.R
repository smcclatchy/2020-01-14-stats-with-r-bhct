# Create matrices
X <- rbind(
  c(3, 4, -5, 1),
  c(2, 2, 2, -1),
  c(1, -1, 5, -5),
  c(5, 0, 0, 1)
)
Y <- c(10, 5, 7, 4)

# use solve function to find inverse of X
X_INV <- solve(X)

# use "%*%" to multiply the inverse of X by Y to find the unknown coefficients
B <- X_INV %*% Y
#            [,1]
# [1,]  1.2477876
# [2,]  1.0176991
# [3,] -0.8849558
# [4,] -2.2389381

# Check
sum(B * X[1,])
# [1] 10
sum(B * X[2,])
# [1] 5
sum(B * X[3,])
# [1] 7
sum(B * X[4,])
# [1] 4


